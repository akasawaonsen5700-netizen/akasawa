const { GoogleGenerativeAI } = require('@google/generative-ai');

exports.handler = async (event) => {
  // CORS対応
  if (event.httpMethod === 'OPTIONS') {
    return {
      statusCode: 200,
      headers: {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Headers': 'Content-Type',
        'Access-Control-Allow-Methods': 'POST, OPTIONS'
      },
      body: ''
    };
  }

  if (event.httpMethod !== 'POST') {
    return json(405, { error: 'Method not allowed' });
  }

  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    return json(500, { error: 'GEMINI_API_KEY is not configured in server environment variables.' });
  }

  try {
    const { type, payload } = JSON.parse(event.body || '{}');

    if (!type || !payload) {
      return json(400, { error: 'type and payload are required.' });
    }

    const genAI = new GoogleGenerativeAI(apiKey);
    const model = genAI.getGenerativeModel({ model: 'gemini-2.5-flash' });

    let systemPrompt = '';

    // タイプ別のプロンプト構築
    if (type === 'rakuten') {
      systemPrompt = `
      あなたは「楽天トラベル」の集客・MEO対策を専門とするホテルコンサルタントです。
      赤沢温泉旅館の現在の掲載情報（楽天のキャッチコピー、紹介文、販売状況など）を分析し、以下の項目を網羅した詳細な改善提案（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【現状の課題】: 楽天トラベルの主要ユーザー（国内レジャー、カップル、ひとり旅など）の視点から、掲載テキストの訴求力が欠けている点、写真やプラン構成の課題を指摘してください。
      2. 【キャッチコピー改善案】: 楽天の「宿名（サブタイトル含む）」および「看板キャッチコピー」の文字数制限に適した、魅力的で検索されやすい改善テキスト案。
      3. 【施設説明文の改善案】: 読んだだけで情景が浮かび、行きたくなるようなリライト文。
      4. 【プロモーション＆クーポン最適化アドバイス】: 
         - 「5と0のつく日」用クーポンの具体的な割引率（5%〜10%の範囲で閑散期・週末の配分案）の推奨値。
         - 「楽天スーパーSALE」や「お買い物マラソン」などの特集参画時の割引率や特別プラン（楽天限定プラン等）の具体的な設計案。
         - 宿たびクーポン等の活用方法。
      5. 【優先アクションプラン】: すぐに管理画面（ITC）で設定すべき行動リスト（3件）。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'jalan') {
      systemPrompt = `
      あなたは「じゃらんnet」の集客・販促を専門とするホテルコンサルタントです。
      赤沢温泉旅館の現在の掲載情報を分析し、じゃらんの媒体特性（若年層、女子旅、温泉こだわり層が多い）に合わせた詳細な改善提案（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【現状の課題】: じゃらんユーザーが温泉や猫の要素に対してどう感じているか、現在のプランや写真の見え方の課題を指摘。
      2. 【キャッチコピー＆紹介文の改善案】: じゃらんの宿名・キャッチコピー（文字制限に適合）の改善テキスト案、およびプラン一覧の上部に載る施設紹介文のリライト案。
      3. 【プロモーション＆クーポン最適化アドバイス】:
         - じゃらんの主要プロモーションツール「じゃらんクーポンフェス」「じゃらんバザール」などの参画優先度と、提供すべきクーポンの値引き原資（例: 総額の3%〜5%程度など）の推奨設定。
         - じゃらん限定プラン（直前割、早期割、ポイントアップキャンペーンなど）の具体的な商品設計。
      4. 【優先アクションプラン】: 管理画面（管理画面）ですぐ設定すべき行動リスト（3件）。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'booking') {
      systemPrompt = `
      あなたは「Booking.com」のインバウンド集客・レートパリティ（料金整合性）を専門とするホテルコンサルタントです。
      赤沢温泉旅館の情報を分析し、外国人旅行客（インバウンド）が「奥日本リトリート」として行きたくなるような改善提案（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【インバウンド目線の課題】: 英語での見え方、写真から伝わる情報の不備、パブリック施設の案内など、海外ゲストにとって分かりにくい点や懸念点（アクセスや畳の文化、食事対応など）を指摘。
      2. 【キャッチコピー＆紹介文の改善案】: Booking.comの施設概要（英語翻訳を意識したシンプルな日本語表現）のリライト案、および写真キャプションの工夫点。
      3. 【プロモーション＆クーポン最適化アドバイス】:
         - **Geniusプログラム（レベル1〜3）**の活用状況と、対象とする部屋タイプ・期間の選定アドバイス。
         - インバウンドに絶大な効果がある「モバイル割引（スマートフォン予約向け10%OFF）」や、特定のターゲット地域に絞った「国別料金（Georate）」の設定推奨。
         - 直前キャンセルを減らすための返金不可料金（ノンリファンダブル）とクーポンの併用設計。
      4. 【優先アクションプラン】: Booking.com管理画面（Extranet）での優先設定リスト（3件）。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'review') {
      systemPrompt = `
      あなたはホテルのカスタマーサクセスおよび品質管理のアナリストです。
      寄せられた複数のクチコミから、「強み」と「現場で改善すべき隠れた課題（オペレーション・ハード面）」を抽出し、具体的な現場改善アクションプラン（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【顧客満足の核心（強み）】: クチコミからわかる、お客様が真に感動している部分（ぬる湯の長湯、猫との時間など）の分析。
      2. 【隠れた運営課題（弱み・不満）】: クチコミににじみ出ている、または明記されている不満（例：清掃が行き届いていない、食事の配膳が早すぎる、お風呂がぬるすぎる等）の本質的な原因分析。
      3. 【現場オペレーション改善提案】: 
         - 配膳タイミングや清掃チェック体制など、今日からできる現場マニュアル・行動の変更案。
         - ハード面（Wi-Fi、トースターの数、スマートキーなど）での物理的改善・投資の必要性に関するアドバイス。
      4. 【優先アクションプラン】: 朝礼や会議ですぐにスタッフと共有し、実施すべき改善タスク（3件）。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'photo') {
      systemPrompt = `
      あなたはホテルのブランディングおよびWEBマーケティング用のビジュアルディレクターです。
      現在のOTA掲載写真リスト（または撮影シチュエーション）を分析し、予約率（CVR）を高めるための具体的な撮影・ビジュアル改善ディレクション（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【ビジュアル面での現在の課題】: 写真の印象（暗さ、ありきたりさなど）、旅館の特徴である「猫」「ぬる湯」「滝」「古民家」の魅力が十分に伝わっていない要因を指摘。
      2. 【改善すべき既存写真の変更案】: 既存のロビー、客室、温泉などの「光の当て方」「アングル」「猫の配置」などをどう変えるべきかのアドバイス。
      3. 【追加撮影すべき推奨カット】: 新しくプロのカメラマン（またはスマホで高画質）に撮影してもらうべき具体的な写真（例：対岸から望む旅館の全景、できたての蒸し餃子から湯気が立っている瞬間、猫が廊下を歩く静寂のワンシーンなど）。
      4. 【画像生成AI用のプロンプト案（2種）】: ホームページやSNSで「こんなイメージの写真がほしい」という時に、画像生成AIに入力してシミュレーションできる美しい英語プロンプトを2パターン作成（JSON内に含める）。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'plan') {
      systemPrompt = `
      あなたはホテルのプラン作成・マーケティングの専門家です。
      既存の宿泊プラン名と説明文を分析し、OTAでのクリック率（CTR）と予約転換率（CVR）を上げるためのリライトおよび付加価値提案（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      **出力フィールド**:
      - "issues": "現在のプランの課題（文字数、フックの弱さなど）",
      - "revisedPlanName": "改善されたプラン名（【楽天限定】などのフックや、ターゲットを明示したプラン名。文字数制限の範囲内）",
      - "revisedDescription": "読んだだけで情景が浮かび、予約ボタンを押したくなる魅力的なリライト説明文",
      - "valueAddProps": "プランに付け加えるべき付加価値特典（例：チェックアウト1時間延長、猫用おやつチュール付き、特製蒸し餃子のおかわり自由など）の提案",
      - "targetAudience": "このプランが最も刺さるターゲット層"

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    } else if (type === 'rank_ad') {
      systemPrompt = `
      あなたはホテルのデジタルマーケティングおよびOTA広告運用（楽天ITC・じゃらん販促など）の専門家です。
      現在の検索順位、KW露出状況、広告運用のデータをもとに、MEO/SEOの向上と広告投資対効果（ROAS）の最大化、および値引きクーポン原資の配分に関する分析（JSONフォーマット）を出力してください。
      
      ■ 入力データ:
      ${JSON.stringify(payload)}

      ■ 分析・出力指示:
      1. 【検索露出（SEO/MEO）の課題】: 現在の検索順位（例：「那須塩原 温泉」「猫のいる宿」など）や露出が低い原因と、それを高めるための対策。
      2. 【強化すべき検索キーワード（タグ）】: 競合が少なく、かつ購買意欲が高いニッチで強力なキーワード（例: 「源泉かけ流し ぬる湯 静養」「古民家 貸切露天風呂」など）の提案。
      3. 【広告（Ad）投資の最適化案】:
         - 楽天トラベルの「キーワード広告」や、じゃらんの「販促パック」等の有料広告の予算を増やすべきか、減らすべきかのシミュレーションアドバイス。
      4. 【クーポン・プロモーション原資の分配最適化案】:
         - 単なる有料広告への投資と、直前割引やクーポンの値引き原資（ポイントアップ等）への投資の、最も効率的な比率（例: 広告費30%、クーポン原資70%など）の推奨提案。

      必ず指定されたJSONフォーマット（プレーンなJSON）のみで出力してください。
      `;
    }

    // JSONで厳格に返却させるためのフォーマット定義
    systemPrompt += `
    ---
    ■ 返却するJSON構造（※'plan'以外の場合も、以下の共通の構造または適切な構造をオブジェクトとしてシリアライズして返してください）:
    {
      "issues": "分析した現状の課題（要約）",
      "revisedText": "リライトされたキャッチコピーや紹介文、撮影カット案など（テキストまたはマークダウン形式のリスト）",
      "promotionAdvice": "【重要】クーポンやプロモーション、広告費、値引き原資の最適活用法や割引率などの具体的アドバイス（マークダウン形式）",
      "actionPlan": [
        "すぐに実施すべきアクション1",
        "すぐに実施すべきアクション2",
        "すぐに実施すべきアクション3"
      ]
    }
    `;

    const result = await model.generateContent({
      contents: [{ role: 'user', parts: [{ text: systemPrompt }] }],
      generationConfig: {
        responseMimeType: 'application/json'
      }
    });

    let responseText = result.response.text().trim();
    if (responseText.startsWith('```')) {
      responseText = responseText.replace(/^```[a-zA-Z]*\n/, '').replace(/\n```$/, '');
    }

    return {
      statusCode: 200,
      headers: {
        'Content-Type': 'application/json; charset=utf-8',
        'Access-Control-Allow-Origin': '*'
      },
      body: responseText
    };

  } catch (error) {
    console.error('OTA Analysis failed:', error);
    return json(500, { error: error.message || 'OTAの分析中に内部エラーが発生しました。' });
  }
};

function json(statusCode, body) {
  return {
    statusCode,
    headers: {
      'Content-Type': 'application/json; charset=utf-8',
      'Access-Control-Allow-Origin': '*'
    },
    body: JSON.stringify(body)
  };
}
